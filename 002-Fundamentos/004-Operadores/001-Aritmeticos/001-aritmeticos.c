#include <stdio.h>

int main(int argc,char *argv[]){
    //SUMA
    printf("suma: %i \n",(4+3));
    //RESTA
    printf("resta: %i \n",(4-3));
    //MULTIPLICACIÓN
    printf("multiplicacion: %i \n",(4*3));
    //DIVISIÓN %f porque es un float
    printf("division: %f \n",(4/3));
    //RESTO ENTERO
    printf("resto entero de la division: %i \n",(4%3));
    
    return 0;
}