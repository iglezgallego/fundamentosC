#include <stdio.h>

int main(int argc,char *argv[]){
    //MAYOR QUE
    printf("mayor que: %i \n",(4>3));
    //MENOR QUE
    printf("menor que: %i \n",(4<3));
    
    //MAYOR O IGUAL
    printf("mayor o igual que: %i \n",(4>=3));
    //MENOR O IGUAL
    printf("menor o igual que: %i \n",(4<=3));
    
    //IGUALDAD
    printf("igualdad: %i \n",(4==3));
    //DESIGUALDAD
    printf("desigualdad: %i \n",(4!=3));
    return 0;
}